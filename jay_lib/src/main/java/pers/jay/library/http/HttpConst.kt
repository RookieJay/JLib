package pers.jay.library.http

/**
 * Http常量
 */
object HttpConst {

    /**
     * 成功
     */
    const val HttpStatus_200 = 200

    /**
     * 参数错误
     */
    const val HttpStatus_400 = 400

    /**
     * 未通过身份认证
     */
    const val HttpStatus_401 = 401

    /**
     * 没有访问对应资源的权限
     */
    const val HttpStatus_403 = 403

    /**
     * 未找到资源
     */
    const val HttpStatus_404 = 404

    /**
     * 服务端错误
     */
    const val HttpStatus_500 = 500
}